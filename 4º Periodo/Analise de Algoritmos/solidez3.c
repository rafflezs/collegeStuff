int solidez(int A[], int p, int r){

    int f, x;
    for(int q = p; q <= r; q++){
        int s = A[r/2] + A[q/2];
        if(s > A[q]){
            f = s;
        }else{
            f = A[q];
        }
        if(f > x){
            x = f;
        }
    }
    
    return x;
}